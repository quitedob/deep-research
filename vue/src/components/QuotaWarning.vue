<script>
import axios from 'axios';

export default {
  name: 'QuotaWarning',
  data() {
    return {
      quota: null,
      dismissed: false,
      checkInterval: null
    };
  },
  computed: {
    shouldShow() {
      if (!this.quota || this.dismissed) return false;
      // ÊòæÁ§∫Êù°‰ª∂ÔºöÈÖçÈ¢ùË∂ÖËøá 80% ÊàñÂ∑≤Ë∂ÖÈôê
      return this.quota.percentage_used >= 80 || this.quota.is_exceeded;
    },
    
    severityClass() {
      if (!this.quota) return '';
      if (this.quota.is_exceeded) return 'severity-error';
      if (this.quota.percentage_used >= 90) return 'severity-warning';
      return 'severity-info';
    },
    
    icon() {
      if (!this.quota) return '';
      if (this.quota.is_exceeded) return 'üö´';
      if (this.quota.percentage_used >= 90) return '‚ö†Ô∏è';
      return '‚ÑπÔ∏è';
    },
    
    title() {
      if (!this.quota) return '';
      if (this.quota.is_exceeded) return 'ÈÖçÈ¢ùÂ∑≤Áî®ÂÆå';
      if (this.quota.percentage_used >= 90) return 'ÈÖçÈ¢ùÂç≥Â∞ÜÁî®ÂÆå';
      return 'ÈÖçÈ¢ùÊèêÈÜí';
    },
    
    message() {
      if (!this.quota) return '';
      return this.quota.warning_message || 'ÊÇ®ÁöÑÈÖçÈ¢ù‰ΩøÁî®ÊÉÖÂÜµ';
    },
    
    progressWidth() {
      if (!this.quota) return '0%';
      return Math.min(100, this.quota.percentage_used) + '%';
    }
  },
  async mounted() {
    await this.checkQuota();
    // ÊØè 30 ÁßíÊ£ÄÊü•‰∏ÄÊ¨°ÈÖçÈ¢ù
    this.checkInterval = setInterval(() => {
      this.checkQuota();
    }, 30000);
  },
  beforeUnmount() {
    if (this.checkInterval) {
      clearInterval(this.checkInterval);
    }
  },
  methods: {
    async checkQuota() {
      try {
        const response = await axios.get('/api/quota/status');
        this.quota = response.data;
        
        // Â¶ÇÊûúÈÖçÈ¢ùÂ∑≤Ë∂ÖÈôêÔºåËß¶ÂèëÂÖ®Â±Ä‰∫ã‰ª∂
        if (this.quota.is_exceeded) {
          this.$emit('quota-exceeded', this.quota);
          // ÂèØ‰ª•Âú®ËøôÈáåÁ¶ÅÁî®Êüê‰∫õÂäüËÉΩ
          this.disableFeatures();
        }
      } catch (error) {
        console.error('Ê£ÄÊü•ÈÖçÈ¢ùÂ§±Ë¥•:', error);
      }
    },
    
    disableFeatures() {
      // Á¶ÅÁî®ÂØπËØùÂíåÁ†îÁ©∂ÂäüËÉΩ
      // ÂèØ‰ª•ÈÄöËøá Vuex Êàñ‰∫ã‰ª∂ÊÄªÁ∫øÈÄöÁü•ÂÖ∂‰ªñÁªÑ‰ª∂
      if (window.EventBus) {
        window.EventBus.emit('disable-chat');
        window.EventBus.emit('disable-research');
      }
    },
    
    handleUpgrade() {
      // Ë∑≥ËΩ¨Âà∞ËÆ¢ÈòÖÈ°µÈù¢
      this.$router.push('/admin?tab=subscription');
    },
    
    dismiss() {
      this.dismissed = true;
      // 5 ÂàÜÈíüÂêéÈáçÊñ∞ÊòæÁ§∫
      setTimeout(() => {
        this.dismissed = false;
      }, 300000);
    }
  }
};
</script>

